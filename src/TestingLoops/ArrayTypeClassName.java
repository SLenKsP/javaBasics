package TestingLoops;

public class ArrayTypeClassName 
{
	public static void main(String[] args) {
		
		//int class type in Array
		int[][] a= new int[3][2];
		System.out.println(a.getClass().getName());
		
		//double class type in Array
		double[][] b= new double[3][2];
		System.out.println(b.getClass().getName());
		
		//short class type in Array
		short[][] c= new short[3][2];
		System.out.println(c.getClass().getName());
		
		//byte class type in Array
		byte[][] d = new byte[3][2];
		System.out.println(d.getClass().getName());
		
		//boolean class type in Array
		boolean[][] x= new boolean[3][2];
		System.out.println(x.getClass().getName());
		
		//long class type in Array
		long[][] e= new long[3][2];
		System.out.println(e.getClass().getName());
		
		//float class type in Array
		float[][] f= new float[3][2];
		System.out.println(f.getClass().getName());
		
		//char class type in Array
		char[][] g= new char[3][2];
		System.out.println(g.getClass().getName());
		
	}
}
