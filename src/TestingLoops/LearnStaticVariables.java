package TestingLoops;

public class LearnStaticVariables {
	static int i;
	static double d;
	static String S;
	static boolean b;

	public static void main(String[] args) {
		//LearnStaticVariables SV= new LearnStaticVariables(); No need to define method here for static variable
		System.out.println(i); //SV can be called without using any class name
		System.out.println(d);
		System.out.println(S);
		System.out.println(b);

	}

}
